             <section id="Como Funciona La Eneergia Hidroelectrica" class="caja mb-4">
                <h2>En pocas palabras, la energía hidroeléctrica transforma la energía del agua en movimiento en electricidad. Este proceso se lleva a cabo en centrales hidroeléctricas que suelen estar conectadas a grandes embalses. Al represar el agua en un embalse, tanto el agua como la energía que contiene se pueden almacenar hasta que las necesitemos (de ahí la flexibilidad).</h2>
                <ul>
                    <li>Las centrales hidroeléctricas sin embalses suelen denominarse centrales de pasada. En estos casos, la producción se controla en función del caudal de agua en cada momento. En ocasiones, varias centrales hidroeléctricas se ubican una tras otra para que la energía del agua se aproveche varias veces antes de que el agua desemboque en el mar.</li>
                    <li>En pocas palabras, la energía hidroeléctrica funciona así:</li>
                    <li>La lluvia y la nieve derretida se acumulan en embalses, a menudo situados en lo alto de las montañas</li>
                    <li>El agua es canalizada desde el embalse hasta la central eléctrica. En ese trayecto, el agua alcanza una gran velocidad. Dentro de la central eléctrica, el agua golpea una turbina que comienza a girar. La turbina está conectada a un eje y a un generador. Cuando la turbina y el eje giran, se genera energía mecánica. Esta energía mecánica se transmite a un generador donde se convierte en electricidad.</li>
                </ul>
            </section>


